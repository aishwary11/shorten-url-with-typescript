import cors from 'cors';
import 'dotenv/config';
import express, { Express, NextFunction, Request, Response } from 'express';
import helmet from 'helmet';
import { validateForm } from './common/utils/formvalidator';
import responseHandler from './common/utils/responsehelpers';
import { deletePost, formUrl, getUrls, login, postUrl, reDirectUrl, signUp } from './controllers/urlController';
import connectDB from './db/config';
import isAuthenticated from './middleware/isauthenticated';
const app: Express = express();
const port = process.env.PORT;
connectDB();
app.use(cors());
app.use(helmet());
app.use(express.json());
app.use((err: Error, req: Request, res: Response, next: NextFunction) => {
  if (err) return responseHandler(res, 500, `Error: ${err.message}`);
  console.log('Url: ', req.url);
  next();
});
app.post('/login', login);
app.post('/signup', signUp);
app.use(isAuthenticated);
app.get('/', getUrls);
app.get('/:urlCode', reDirectUrl);
app.post('/shorten', postUrl);
app.delete('/:urlCode', deletePost);
app.post('/form', validateForm, formUrl);
app.listen(port, () => console.log(`Server Started at ${port}`));
